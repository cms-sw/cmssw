          xorptl(3,i)=z
          xorptl(4,i)=t
        endif
      enddo


      if(ish.ge.5)then
        do k=1,5
          pptl(k,nptl+nall+2)=0
          do ii=nptlbcf+1,nptl
            pptl(k,nptl+nall+2)=pptl(k,nptl+nall+2)+pptl(k,ii)
