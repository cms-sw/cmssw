ESDetId::ESDetId() : DetId() 
{
}
  
ESDetId::ESDetId( uint32_t rawid ) : 
   DetId( rawid ) 
{
}
  
ESDetId::ESDetId( int strip,
		  int ixs,
		  int iys,
		  int plane,
		  int iz     ) :
   DetId( Ecal, EcalPreshower ) 
{
   if( !validDetId( strip, ixs, iys, plane, iz) )
      throw cms::Exception("InvalidDetId") 
	 << "ESDetId:  Cannot create object.  Indexes out of bounds \n" 
	 << " strip = " << strip << " x = " << ixs << " y = " << iys << "\n" 
	 << " plane = " << plane << " z = " << iz
	 << " hxy = " << (1==plane?hxy1[ixs-1][iys-1]:hxy2[ixs-1][iys-1])
	 << "\n";

   id_ |=
      (strip&0x3F) |
      ((ixs&0x3F)<<6) |
      ((iys&0x3F)<<12) |
      (((plane-1)&0x1)<<18) |
      ((iz>0)?(1<<19):(0));
}
  
ESDetId::ESDetId( const DetId& gen ) 
{
   if( !gen.null() && 
       ( gen.det()      != Ecal          ||
	 gen.subdetId() != EcalPreshower    ) ) 
   {
      throw cms::Exception("InvalidDetId");
   }
   id_ = gen.rawId() ;
}

bool 
ESDetId::validDetId( int istrip, 
		     int ixs, 
		     int iys, 
		     int iplane, 
		     int iz      ) 
{
  return ( !( ( istrip < ISTRIP_MIN ) ||
	      ( istrip > ISTRIP_MAX ) ||
	      ( ixs    < IX_MIN     ) || 
	      ( ixs    > IX_MAX     ) ||
	      ( iys    < IY_MIN     ) ||
	      ( iys    > IY_MAX     ) ||
	      ( abs( iz ) != 1      ) ||
	      ( iplane < PLANE_MIN  ) ||
	      ( iplane > PLANE_MAX  ) ||
	      ( ( 1 == iplane ) &&
		0 == hxy1[ixs-1][iys-1]  ) ||
	      ( ( 2 == iplane ) &&
		0 == hxy2[ixs-1][iys-1]  )    ) ) ;
}
  
ESDetId& 
ESDetId::operator=( const DetId& gen ) 
{
   if (!gen.null() &&
       ( gen.det()      != Ecal          ||
	 gen.subdetId() != EcalPreshower    ) ) 
   {
      throw cms::Exception("InvalidDetId");
   }
   id_=gen.rawId();
   return *this;
}

int 
ESDetId::hashedIndex() const 
{
   const int ia ( 1 == zside() ? 2 : 1 ) ;
   const int ib ( plane() ) ;
   const int ix ( six() ) ;
   const int iy ( siy() ) ;
   const int ic ( 1 == ib ?
		  hxy1[ ix - 1 ][ iy - 1 ] :
		  hxy2[ ix - 1 ][ iy - 1 ] ) ;
   const int id ( strip() ) ;

   return ( ( ia - 1 )*kLb +
	    ( ib - 1 )*kLc +
	    ( ic - 1 )*kLd + 
	    id             - 1 ) ;
}

ESDetId 
ESDetId::unhashIndex( int hi ) 
{
   if( validHashIndex( hi ) )
   {
      const int id ( hi%kLd + 1 ) ;
      const int nd ( hi - id + 1 ) ;
      const int ic ( ( nd%kLc )/kLd + 1 ) ;
      const int nc ( nd - ( ic - 1 )*kLd ) ;
      const int ib ( ( nc%kLb )/kLc + 1 ) ;
      const int nb ( nc - ( ib - 1 )*kLc ) ;
      const int ia ( nb/kLb + 1 );// + 1 ) ;

      const int st    ( id ) ;
      const int pl    ( ib ) ;
      int ix ;
      int iy ;
      const int ic1 ( ic - 1 ) ;
      if( 1 == pl )
      {
	 ix = hx1[ ic1 ] ;
	 iy = hy1[ ic1 ] ;
      }
      else
      {
	 ix = hx2[ ic1 ] ;
	 iy = hy2[ ic1 ] ;
      }
      const int iz ( 1 == ia ? -1 : 1 ) ;

      return ESDetId( st, ix, iy, pl, iz ) ;
   }
   else
   {
      return ESDetId() ;
   }
}
  
